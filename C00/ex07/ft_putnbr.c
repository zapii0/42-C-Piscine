#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	b;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (nb < 0)
	{
		nb = (nb * -1);
		write(1, "-", 1);
		ft_putnbr(nb);
	}
	else if (nb < 10)
	{
		b = nb + '0';
		write(1, &b, 1);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}
