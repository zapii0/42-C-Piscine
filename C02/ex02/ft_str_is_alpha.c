int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] < 91 && str[i] > 64)
			i++;
		else if (str[i] < 123 && str[i] > 96)
			i++;
		else
			return (0);
	}
	return (1);
}
